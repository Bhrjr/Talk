                              tx["md"] = "2" if $msg.tag == "夢魔がパートナーを脱衣" #脱衣できなかった
                              tx["md"] = "3" if $msg.tag == "パートナーが夢魔を脱衣" #相手を脱衣させた
                            when 3 #レジスト失敗
                              tx["md"] = "3" if $msg.tag == "夢魔がパートナーを脱衣" #脱衣させられた
                              tx["md"] = "2" if $msg.tag == "パートナーが夢魔を脱衣" #相手を脱衣できなかった
                            end
case $msg.tag
#====================================================================================================================
#■夢魔がパートナーを脱衣させる
#====================================================================================================================
when "夢魔がパートナーを脱衣"
  case $msg.talk_step
  when 1 #脱衣開始
    tx["tx1"] = "「#{giggle}\n　さ、脱がせてやるからじっとしてな？」"
    tx["tx1"] = "「#{giggle}\n　さ、脱がせてやるからじっとしてな？」" if $msg.t_enemy.nude? #夢魔が先に脱いでいた
  #---------------------------------------------------
  when 2 #パートナーが抵抗した
    tx["tx1"] = "「なんだよ、別に良いじゃないか。\n　女同士で減るもんじゃなし」"
    tx["tx1"] = "「なんだよ、別に良いじゃないか。\n　女同士で減るもんじゃなし」" if $msg.t_enemy.nude? #夢魔が先に脱いでいた
  #---------------------------------------------------
  when 3 #パートナーが脱衣した
    tx["tx1"] = "「おっと、乗り気だね♪」"
    #▼自ら受け入れた場合
    tx["tx1"] = "「おっと、乗り気だね♪」" if $game_switches[89] == true
    tx["tx1"] = "「ふふ、可愛いじゃないか。\n　ほら、もっとよく見せてみな？」" if $msg.t_target.age <= 2
  #---------------------------------------------------
  end
  #格納
  ms.push(tx)
#====================================================================================================================
#■パートナーが夢魔を脱衣させる
#====================================================================================================================
when "パートナーが夢魔を脱衣"
  case $msg.talk_step
  when 1 #脱衣開始
    tx["tx1"] = "「おっと、脱がせてくれるって？」"
    tx["tx1"] = "「おっと、#{myname}も脱げって？」" if $msg.t_target.nude? #アクターが先に脱いでいた
  #---------------------------------------------------
  when 2 #夢魔を脱衣させた
    tx["tx1"] = "「ふふ、ありがとさん。\n　この礼はちゃんと身体で返さないとな\\H」"
    tx["tx1"] = "「そら、これでいいかい？\n　んじゃ、続きと行こうか\\H」" if $msg.t_target.nude? #アクターが先に脱いでいた
  #---------------------------------------------------
  when 3 #夢魔に抵抗された
    tx["tx1"] = "「ふふ、まあ少し待ちなよ。\n　もう少しお互い楽しもうぜ」"
    #途中で行動をキャンセルした場合
    tx["tx1"] = "「何だよ、止めるのかい？\n　別に構わなかったんだがな」" if $game_switches[89] == true
  #---------------------------------------------------
  end
  #格納
  ms.push(tx)
#====================================================================================================================
#■パートナーが自ら脱衣する
#====================================================================================================================
when "パートナーが自ら脱衣"
  tx["tx1"] = "「へぇ……結構思い切りいいね。\n　こいつは楽しめそうだ……♪」"
  tx["tx1"] = "「へぇ……結構思い切りいいね。\n　こいつは楽しめそうだ……♪」" if $msg.t_enemy.nude? #夢魔が先に脱いでいた
  #---------------------------------------------------
  #格納
  tx["md"] = "3"
  ms.push(tx)
#====================================================================================================================
#■夢魔が自ら脱衣する
#====================================================================================================================
when "夢魔が自ら脱衣"
  #------------------------------------------------------------------------------------------------------------------
  tx["tx1"] = "「#{giggle}\n　さ、続きといこうか」"
  tx["tx1"] = "「ふふ、待たせたかな？\n　さ、続きといこうか」" if $msg.t_target.nude? #アクターが先に脱いでいた
  #---------------------------------------------------
  #格納
  tx["md"] = "3"
  ms.push(tx)
#----------------------------------------------------------------------------------------------------------------------
end #case $msg.tag
#----------------------------------------------------------------------------------------------------------------------
 end #def
end #class










